package ejercicio.basico.principiantes;

public class ej5IncrementoDecremento {

	public static void main(String[] args) {

		// ejercicio1

		int x = 10;
		System.out.println(x);
		x++;
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x++);
		System.out.println(x);
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(++x);
		++x;
		x++;
		System.out.println(++x);
		System.out.println(x++);
		System.out.println(++x);

		char a = 'c';
		char b;
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(++a);
		b = --a;
		System.out.println(++a);
		a++;
		--b;
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(++a);
		System.out.println(b--);
		System.out.println(a);
		System.out.println(b);

	}

}
